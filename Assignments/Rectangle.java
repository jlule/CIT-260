
public class Rectangle {
	 
			private double height = 1;
			private double width = 1;
			
			public Rectangle(double width, double height) {
				this.width = width;
				this.height = height;
			}
			public double getarea() {
				return width *height;
				}
			public double getPerimeter() {
				return (2*width)+(2*height) ;
			}
			
			

}
